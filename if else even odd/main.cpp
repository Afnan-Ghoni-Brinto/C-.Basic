#include <iostream>
using namespace std;
int main()
{
    int number;
    cout<<"Give me the number you want to know "<<endl;
    cin>>number;
    cout<<"         SO"<<endl;
    if(number%2==0){
        cout<<"The number  "<<number <<"  is even "<<endl;
    }
    else{
        cout<<"The number  "<<number <<"  is odd"<<endl;
    }

    return 0;
}
